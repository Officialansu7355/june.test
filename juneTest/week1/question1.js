// Function declaration 

function add(a, b) {         

    console.log(a + b);
}
 
// Calling a function
add(2, 3)


// function expression

const add1 = function(a, b) {

    console.log(a+b);
} 
 
// calling function
add1(2, 3);



// Single line of code
let add2 = (a, b) => a + b; 
 
console.log(add2(3, 2));